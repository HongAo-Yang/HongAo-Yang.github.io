			<p class="p-4 space-y-4 text-justify">
				In this work, we identify a new class of planar nodal chains in non-symmorphic phononic
				systems, where the connecting rings lie in the same plane. The constituting nodal rings are
				protected by mirror symmetry, and their intersection is guaranteed by the combination of
				time-reversal and non-symmorphic twofold screw symmetry. The connecting points are fourfold
				degenerate while those in previous works are twofold degenerate. We found 8 out of 230 space
				groups that can host the proposed planar nodal chain phonons. Taking wurtzite GaN (space
				group No. 186) as an example, the planar nodal chain is confirmed by first-principles
				calculations. The planar nodal chains result in two distinct classes of drumhead surface
				states on the [10(–1)0] and the [0001] surface Brillouin zones.
			</p>
