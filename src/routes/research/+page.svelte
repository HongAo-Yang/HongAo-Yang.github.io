				<p class="p-4 space-y-4 text-center">
					<strong
						>Lattice Dynamics Method for Mode-resolved Phonon Transmittance Across Interface</strong
					>
				</p>
			</header>
			<div class="mx-auto flex items-center justify-center">
				<img
					src="/research/2023_JAP_LatticeDynamics/graphical_abstract.jpg"
					alt="2023_JAP_LatticeDynamics"
					class="w-3/5"
				/>
			</div>
			<p class="p-4 space-y-4 text-justify">
				Lattice dynamics (LD) enables the calculation of mode-resolved transmittance of phonons
				passing through an interface, which is essential for understanding and controlling the
				thermal boundary conductance (TBC). However, the original LD method may yield unphysical
				transmittance over 100% due to the absence of the constraint of energy conservation. Here,
				we present a robust LD algorithm that utilizes linear algebra transformations and projection
				gradient descent iterations to ensure energy conservation. The evanescent modes and
				localized effects at the interface are revealed. In addition, bottom-up analysis of the
				phonon transmittance shows that the anisotropy in the azimuth angle can be ignored, while
				the dependency on the frequency and polar angle can be decoupled. The decoupled expression
				reproduces the TBC precisely.
			</p>
