			<p class="p-4 space-y-4 text-justify">
				Existing machine learning potentials for predicting phonon properties of crystals are
				typically limited on a material-to-material basis, primarily due to the exponential scaling
				of model complexity with the number of atomic species. We address this bottleneck with the
				developed Elemental Spatial Density Neural Network Force Field, namely Elemental-SDNNFF. The
				effectiveness and precision of our Elemental-SDNNFF approach are demonstrated on 11,866
				full, half, and quaternary Heusler structures spanning 55 elements in the periodic table by
				prediction of complete phonon properties. Self-improvement schemes including active learning
				and data augmentation techniques provide an abundant 9.4 million atomic data for training.
				Deep insight into predicted ultralow lattice thermal conductivity of 774 Heusler structures
				is gained by p–d orbital hybridization analysis. Additionally, a class of two-band charge-2
				Weyl points, referred to as "double Weyl points", are found in 68% and 87% of 1662 half and
				1550 quaternary Heuslers, respectively.
			</p>
