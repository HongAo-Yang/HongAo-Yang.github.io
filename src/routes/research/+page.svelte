				<p class="p-4 space-y-4 text-center">
					<strong>Topological Protected Planar Nodal Chain Phonons </strong>
				</p>
			</header>
			<div class="mx-auto flex items-center justify-center">
				<img
					src="/research/2023_JAP_TopoPhon/graphical_abstract.jpg"
